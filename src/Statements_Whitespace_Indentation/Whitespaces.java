package Statements_Whitespace_Indentation;

public class Whitespaces {
    public static void main(String[] args) {
        int var=5;
        //in this above statement we can see that the expression is very
        // collapsed with each other and hard to read
        //That's why we use whitespaces in java for better readability of code

        int     var2     =    5;
        // this is better cause java automatically ignores the whitespaces in the code and run without errors
    }
}
